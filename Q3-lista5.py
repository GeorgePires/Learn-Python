"""
Verifique se um numero inteiro digitado pelo usuário está fora do
intervalo entre 1 e 10
"""
valor = int(input("Digite um valor: "))

if valor <1 or valor >10:
    print("Esta fora do intervalo")
else :
    print("Esta dentro do intervalo")



